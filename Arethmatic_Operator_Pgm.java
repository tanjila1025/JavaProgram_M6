class Arethmatic_Operator_Pgm{
public static void main(String []args){
 int a=100;
 int b=50;

 int c=a+b;//addition of two operands.
 System.out.println(c);

 int d=a*b;//multiplication of two operands.
 System.out.println(d);
 
 int f=a-b;//substraction of two operands.
 System.out.println(f);

 int g=a/b;//division of two operands..division always return quotient.
 System.out.println(g);

 float k=a%b;//modulus of two operand..modulus always return reminder. 
 System.out.println(k);

 }
}
